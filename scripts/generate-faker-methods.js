const faker = require('faker');
const fs = require('fs');
const path = require('path');

const methods = [];

for (const namespace in faker) {
  if (faker.hasOwnProperty(namespace) && typeof faker[namespace] === 'object') {
    for (const method in faker[namespace]) {
      if (
        faker[namespace].hasOwnProperty(method) &&
        typeof faker[namespace][method] === 'function'
      ) {
        methods.push(`${namespace}.${method}`);
      }
    }
  }
}

const outputPath = path.resolve(__dirname, '../src/faker-methods.ts');
const fileContent = `// This file is auto-generated by scripts/generate-faker-methods.js
// Do not edit this file directly.

export const fakerMethods: string[] = ${JSON.stringify(methods, null, 2)};
`;

fs.writeFileSync(outputPath, fileContent);

